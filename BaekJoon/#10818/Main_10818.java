package DW_Kim_JAVA_Study_BaekJoon;

import java.util.Scanner;

/**
 * author : Kim Do Wan
 * 일      자 : 2020. 10. 24.
 * 문제 제목 : 최소, 최대
 * 문제 번호 : 10818
 * 문제 출처 : https://www.acmicpc.net/
 * 
 * 문      제 : N개의 정수가 주어진다. 이때, 최솟값과 최댓값을 구하는 프로그램을 작성하시오.
 *
 * 입      력 : 첫째 줄에 정수의 개수 N (1 ≤ N ≤ 1,000,000)이 주어진다. 둘째 줄에는 N개의 정수를 공백으로 구분해서 주어진다. 
 *           모든 정수는 -1,000,000보다 크거나 같고, 1,000,000보다 작거나 같은 정수이다.
 *
 * 출      력 : 첫째 줄에 주어진 정수 N개의 최솟값과 최댓값을 공백으로 구분해 출력한다.
 *
 * 
 **/
public class Main_10818 {
	
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        
        int N = sc.nextInt();	//N개의 수를 입력받는다.
        int []A = new int [N];
        
        for(int i = 0; i < A.length; i++) {
        	A[i] = sc.nextInt();
        }

        int max = A[0];			//최댓값 변수 선언.
        int min = A[0];			//최솟값 변수 선언.
        
        //최대 최소를 구한다.
        for(int i = 0; i < A.length; i++) {
        	if(A[i] < min) {
        		min = A[i];
        	} else if(A[i] > max) {
        		max = A[i];
        	}
        }
        
        System.out.print(min + " ");
        System.out.print(max);
    }
}
